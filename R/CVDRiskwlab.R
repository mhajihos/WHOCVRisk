#'   \item Age. Age including age groups: 40-44, 45-49, 50-54, 55-59, 60-64, 65-69, 70-74.
#'   \item Cholesterol. Total cholesterol in (mmol/l) including <4, 4-4.9, 5-5.9, 6-6.9, >=7.
